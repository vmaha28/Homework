package fast.track;

public class Person {
    private String name;
    private int age;
    private String married;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public String getMarried(){
        return married;
    }

    public void setMarried(String married) {
        this.married = married;
    }
}

